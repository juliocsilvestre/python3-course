# Tipo de dado bool (booleano)
# Ao questionar algo em um programa,
# só existem duas respostas possíveis: True ou False
# Existem vários operadores para "questionar" algo em Python
# Dentre eles o ==, que é um operador lógico que questionar se um valor é igual ao outro

print (10 == 10) # True -> Sim (Verdadeiro)
print (10 == 20) # False -> Não (Falso)
print(type(10 == 10)) # bool
print(type(10 == 20)) # bool
print(type(True)) # bool
print(type(False)) # bool