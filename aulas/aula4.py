# Tipos int e float
# int -> números inteiros
# o tipo int representa qualquer número inteiro, positivo ou negativo, sem casas decimais
# int sem sinal é considerado positivo
print (11) #int
print (-11) #int
print (0) #int

# float -> números decimais, com ponto flutuante
# O tipo float representa qualquer número real, positivo ou negativo, com casas decimais / ponto flutuante
# float sem sinal é considerado positivo
print(1.1) #float
print(10.1, 10.2, 10.3) #float

# A função type sempre mostra o tipo que o python inferiu ao valor
print(type(10))
print(type("10"))
print(type(10.0))
print(type(0))
print(type(-10))